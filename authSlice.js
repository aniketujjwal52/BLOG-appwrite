import { createSlice } from "@reduxjs/toolkit";

const initialState = {
    status : false,
    userData: null
}

const authSlice = createSlice({
    name: "auth",
    initialState,
    reducers: {
        login: (state, action) => {
            state.status = true;
            state.userData = action.payload.userData;
        },
        logout: (state) => {
            state.status = false;
            state.userData = null;
        }
     }
})

export const {login, logout} = authSlice.actions;// yeh hum action ko export kar rahe hain taake hum inhe components mein use kar saken aur reducers ko call kar saken uske liye reducers ka naam likhna zaroori hai

export default authSlice.reducer;